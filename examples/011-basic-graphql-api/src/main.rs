/// GET / — overview of the GraphQL API example.
async fn index() -> &'static str {
    "VIL GraphQL API Example\n\
     ===========================\n\n\
     This example demonstrates auto-generated GraphQL schemas from entity definitions.\n\n\
     Endpoints:\n\
     - GET  /api/schema    — view the auto-generated GraphQL schema\n\
     - GET  /api/entities  — list registered entities\n\
     - POST /api/query     — in-memory GraphQL query (accepts JSON)\n\n\
     Built-in:\n\
     - GET  /health, /ready, /metrics, /info\n"
}

/// GET /api/schema — describe the auto-generated GraphQL schema.
async fn schema_info() -> VilResponse<SchemaInfoResponse> {
    let builder = build_schema();
    let description = builder.describe_schema();
    let config = builder.config();

    VilResponse::ok(SchemaInfoResponse {
        graphql_schema: GraphqlSchemaDetail {
            entity_count: builder.entity_count(),
            types: description.types,
            queries: description.queries,
            mutations: description.mutations,
        },
        config: GraphqlConfigDetail {
            playground_enabled: config.playground,
            introspection_enabled: config.introspection,
            max_depth: config.max_depth,
            max_complexity: config.max_complexity,
            default_page_size: config.default_page_size,
            max_page_size: config.max_page_size,
        },
        note: "In production, this schema is served at /graphql with full execution support.".to_string(),
    })
}

/// GET /api/entities — list registered entities and their fields.
async fn list_entities() -> VilResponse<EntityListResponse> {
    let builder = build_schema();

    let entities = vec![
        EntityDetail {
            name: "Order".to_string(),
            table: "orders".to_string(),
            source: "primary".to_string(),
            primary_key: "id".to_string(),
            fields: vec![
                "id: Int!".into(), "customer_id: Int!".into(), "total: Float!".into(),
                "status: String!".into(), "created_at: String!".into(),
            ],
            auto_generated: AutoGenerated {
                queries: vec![
                    "order(id: Int!): Order".into(),
                    "orders(limit: Int, offset: Int): [Order!]!".into(),
                    "orderCount: Int!".into(),
                ],
                mutations: vec![
                    "createOrder(input: CreateOrderInput!): Order!".into(),
                    "updateOrder(id: Int!, input: UpdateOrderInput!): Order!".into(),
                    "deleteOrder(id: Int!): Boolean!".into(),
                ],
            },
        },
        EntityDetail {
            name: "Customer".to_string(),
            table: "customers".to_string(),
            source: "primary".to_string(),
            primary_key: "id".to_string(),
            fields: vec![
                "id: Int!".into(), "name: String!".into(),
                "email: String!".into(), "tier: String!".into(),
            ],
            auto_generated: AutoGenerated {
                queries: vec![
                    "customer(id: Int!): Customer".into(),
                    "customers(limit: Int, offset: Int): [Customer!]!".into(),
                    "customerCount: Int!".into(),
                ],
                mutations: vec![
                    "createCustomer(input: CreateCustomerInput!): Customer!".into(),
                    "updateCustomer(id: Int!, input: UpdateCustomerInput!): Customer!".into(),
                    "deleteCustomer(id: Int!): Boolean!".into(),
                ],
            },
        },
    ];

    VilResponse::ok(EntityListResponse {
        registered_entities: builder.entity_names(),
        entity_count: builder.entity_count(),
        entities,
    })
}

/// Request body for the in-memory GraphQL query endpoint.
#[derive(Debug, Deserialize)]
#[allow(dead_code)]
struct GraphQLRequest {
    query: String,
    #[serde(default)]
    variables: Option<serde_json::Value>,
}

/// POST /api/query — in-memory GraphQL query endpoint.
/// Parses the query string and returns example data based on detected entity names.
async fn graphql_query(
    body: ShmSlice,
) -> HandlerResult<VilResponse<QueryResponse>> {
    let req: GraphQLRequest = body.json().expect("invalid JSON body");
    let query_lower = req.query.to_lowercase();

    // InMemory responses based on query content.
    // In production, VilSchemaBuilder compiles these into real DB queries.
    // The example data below represents typical catalog query results.
    if query_lower.contains("order") {
        Ok(VilResponse::ok(QueryResponse {
            data: serde_json::json!({
                "orders": [
                    { "id": 1, "customer_id": 101, "total": 249.99, "status": "shipped", "created_at": "2025-01-15T10:30:00Z" },
                    { "id": 2, "customer_id": 102, "total": 89.50, "status": "pending", "created_at": "2025-01-16T14:20:00Z" },
                    { "id": 3, "customer_id": 101, "total": 450.00, "status": "delivered", "created_at": "2025-01-17T09:00:00Z" },
                ]
            }),
            extensions: QueryExtensions { complexity: Some(3), depth: Some(1), is_demo: true },
        }))
    } else if query_lower.contains("customer") {
        Ok(VilResponse::ok(QueryResponse {
            data: serde_json::json!({
                "customers": [
                    { "id": 101, "name": "Alice Johnson", "email": "alice@example.com", "tier": "gold" },
                    { "id": 102, "name": "Bob Smith", "email": "bob@example.com", "tier": "silver" },
                ]
            }),
            extensions: QueryExtensions { complexity: Some(2), depth: Some(1), is_demo: true },
        }))
    } else if query_lower.contains("__schema") || query_lower.contains("introspection") {
        let builder = build_schema();
        let desc = builder.describe_schema();
        Ok(VilResponse::ok(QueryResponse {
            data: serde_json::json!({
                "__schema": {
                    "types": desc.types,
                    "queryType": { "name": "Query", "fields": desc.queries },
                    "mutationType": { "name": "Mutation", "fields": desc.mutations },
                }
            }),
            extensions: QueryExtensions { complexity: None, depth: None, is_demo: true },
        }))
    } else {
        Err(VilError::bad_request(format!(
            "InMemory endpoint: unrecognized query. Try querying 'orders' or 'customers'. Received: {}",
            req.query
        )))
    }
}

// ---------------------------------------------------------------------------
// Main
// ---------------------------------------------------------------------------

#[tokio::main]
async fn main() {
    // VX: Define GraphQL service as a Process.
    // The catalog API is a single process — in production you might split
    // read-heavy catalog queries from write-heavy order mutations.
    let graphql_service = ServiceProcess::new("graphql")
        .endpoint(Method::GET, "/", get(index))
        .endpoint(Method::GET, "/schema", get(schema_info))
        .endpoint(Method::GET, "/entities", get(list_entities))
        .endpoint(Method::POST, "/query", post(graphql_query));

    // VX: Run as Process-Oriented app
    VilApp::new("graphql-api")
        .port(8080)
        .service(graphql_service)
        .run()
        .await;
}
