// BUSINESS CONTEXT:
//   E-commerce product catalog API serving the storefront and admin dashboard.
//   GraphQL enables flexible queries: the storefront fetches minimal product
//   cards (id, name, price), while the admin dashboard queries full details
//   with nested customer + order history. Schema auto-generation from entities
//   keeps the API in sync with the database without manual resolver wiring.
//
// Demonstrates vil_graphql's VilSchemaBuilder to auto-generate a GraphQL
// schema from entity definitions, using the VX Process-Oriented architecture
// (VilApp + ServiceProcess).
//
// VX highlights:
//   - ServiceProcess groups endpoints as a logical Process
//   - VilApp orchestrates processes with Tri-Lane mesh
//   - Handlers stay EXACTLY the same as classic vil-server
//
// Routes:
//   GET  /              → overview page (text)
//   GET  /schema        → describes the GraphQL schema generated from entities
//   GET  /entities      → list registered entities (Order, Customer)
//   POST /query         → in-memory GraphQL query endpoint (JSON → example data)
//
// Built-in endpoints (auto-provided by VilApp):
//   GET  /health        → health check
//   GET  /ready         → readiness probe
//   GET  /metrics       → Prometheus-style metrics
//   GET  /info          → server info
//
// Run:
//   cargo run -p basic-usage-graphql-api
//
// Test:
//   curl http://localhost:8080/api/graphql/
//   curl http://localhost:8080/api/graphql/schema
//   curl http://localhost:8080/api/graphql/entities
//   curl -X POST http://localhost:8080/api/graphql/query \
//     -H 'Content-Type: application/json' \
//     -d '{"query":"{ orders { id total } }"}'
//   curl http://localhost:8080/health
// =============================================================================

use vil_server::prelude::*;
use vil_graphql::{GraphQLConfig, VilSchemaBuilder};
use vil_graphql::schema::{EntityDef, FieldDef};

// ---------------------------------------------------------------------------
// Domain models — typed response structs
// ---------------------------------------------------------------------------

/// Response for GET /api/schema.
#[derive(Clone, Debug, Serialize, Deserialize, VilModel)]
struct SchemaInfoResponse {
    graphql_schema: GraphqlSchemaDetail,
    config: GraphqlConfigDetail,
    note: String,
}

#[derive(Clone, Debug, Serialize, Deserialize, VilModel)]
struct GraphqlSchemaDetail {
    entity_count: usize,
    types: Vec<String>,
    queries: Vec<String>,
    mutations: Vec<String>,
}

#[derive(Clone, Debug, Serialize, Deserialize, VilModel)]
struct GraphqlConfigDetail {
    playground_enabled: bool,
    introspection_enabled: bool,
    max_depth: usize,
    max_complexity: usize,
    default_page_size: usize,
    max_page_size: usize,
}

/// Response for GET /api/entities.
#[derive(Clone, Debug, Serialize, Deserialize, VilModel)]
struct EntityListResponse {
    registered_entities: Vec<String>,
    entity_count: usize,
    entities: Vec<EntityDetail>,
}

#[derive(Clone, Debug, Serialize, Deserialize, VilModel)]
struct EntityDetail {
    name: String,
    table: String,
    source: String,
    primary_key: String,
    fields: Vec<String>,
    auto_generated: AutoGenerated,
}

#[derive(Clone, Debug, Serialize, Deserialize, VilModel)]
struct AutoGenerated {
    queries: Vec<String>,
    mutations: Vec<String>,
}

/// Response for POST /api/query (success cases).
#[derive(Clone, Debug, Serialize, Deserialize, VilModel)]
struct QueryResponse {
    data: serde_json::Value,
    extensions: QueryExtensions,
}

#[derive(Clone, Debug, Serialize, Deserialize, VilModel)]
struct QueryExtensions {
    #[serde(skip_serializing_if = "Option::is_none")]
    complexity: Option<u32>,
    #[serde(skip_serializing_if = "Option::is_none")]
    depth: Option<u32>,
    is_demo: bool,
}

// ---------------------------------------------------------------------------
// GraphQL configuration — mirrors vil-server.yaml graphql section
// ---------------------------------------------------------------------------
// max_depth=10 prevents deeply nested queries that could DoS the catalog.
// max_complexity=1000 bounds the cost of queries joining Order+Customer.

/// Build the GraphQL configuration for this service.
fn graphql_config() -> GraphQLConfig {
    GraphQLConfig {
        enabled: true,
        playground: true,
        max_depth: 10,
        max_complexity: 1000,
        introspection: true,
        default_page_size: 20,
        max_page_size: 100,
    }
}

/// Build the schema builder with registered entities.
/// In production, entities come from #[derive(VilEntity)] macros;
/// here we register them manually to show the API surface.
///
/// Business entities:
///   Order  — purchase transaction (links customer, tracks fulfillment status)
///   Customer — buyer profile with loyalty tier (gold/silver/bronze)
fn build_schema() -> VilSchemaBuilder {
    VilSchemaBuilder::new(graphql_config())
        .entity(EntityDef {
            name: "Order".to_string(),
            table: "orders".to_string(),
            source: "primary".to_string(),
            primary_key: "id".to_string(),
            fields: vec![
                FieldDef { name: "id".into(), graphql_type: "Int!".into() },
                FieldDef { name: "customer_id".into(), graphql_type: "Int!".into() },
                FieldDef { name: "total".into(), graphql_type: "Float!".into() },
                FieldDef { name: "status".into(), graphql_type: "String!".into() },
                FieldDef { name: "created_at".into(), graphql_type: "String!".into() },
            ],
        })
        .entity(EntityDef {
            name: "Customer".to_string(),
            table: "customers".to_string(),
            source: "primary".to_string(),
            primary_key: "id".to_string(),
            fields: vec![
                FieldDef { name: "id".into(), graphql_type: "Int!".into() },
                FieldDef { name: "name".into(), graphql_type: "String!".into() },
                FieldDef { name: "email".into(), graphql_type: "String!".into() },
                FieldDef { name: "tier".into(), graphql_type: "String!".into() },
            ],
        })
}

